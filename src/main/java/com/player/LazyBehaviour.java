class LazyBehaviour extends EntityBehaviour {

  LazyBehaviour(final Entity entity, final Board board) {
    super(entity, board);
    this.NAME = "Lazy";
  }

  @Override Action getNextAction() {
    return returnAction(Action.none());
  }
}
